def dividir (d, e):
    d = d / e
    return(d)


def mult (d, e):
    m = d * e
    return(m)



def soma (d, e):
    s = d + e
    return(s)


def sub (d, e):
    sub = d - e
    return(sub)



def metro_centimetro(m):
    return m * 100


def centimetro_metro(cm):
    return cm / 100


def km_metros(km):
    return km * 1000